    private void btnParcourirDossierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParcourirDossierActionPerformed
        // TODO add your handling code here:
        JFileChooser ChoixFichierVel = new JFileChooser("./");
        ChoixFichierVel.setDialogTitle("Choisissez le dossier à envoyer");
        ChoixFichierVel.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);// fichiers seulement
        ChoixFichierVel.setAcceptAllFileFilterUsed(false);// ne pas mettre de filtre de fichier
        ChoixFichierVel.setApproveButtonText("Sélectionner dossier"); //intitule du bouton

        if (ChoixFichierVel.showOpenDialog(null) == JFileChooser.APPROVE_OPTION){
            tfParcourirDossier.setText("");
            File selectedDir = ChoixFichierVel.getSelectedFile();
            if ( !selectedDir.isDirectory() ) {
                selectedDir = selectedDir.getParentFile();
            }
            String MonFichierVel = selectedDir.getPath();
            tfParcourirDossier.setText(MonFichierVel);

        }
    }//GEN-LAST:event_btnParcourirDossierActionPerformed

