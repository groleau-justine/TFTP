    private void btnParcourirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParcourirActionPerformed

        JFileChooser ChoixFichierVel = new JFileChooser("./");
        ChoixFichierVel.setDialogTitle("Choisissez le fichier à envoyer");
        ChoixFichierVel.setFileSelectionMode(JFileChooser.FILES_ONLY);// fichiers seulement
        ChoixFichierVel.setAcceptAllFileFilterUsed(false);// ne pas mettre de filtre de fichier
        ChoixFichierVel.setApproveButtonText("Sélectionner fichier"); //intitule du bouton

        if (ChoixFichierVel.showOpenDialog(null) == JFileChooser.APPROVE_OPTION){
            tfParcourir.setText("");
            String MonFichierVel = (ChoixFichierVel.getSelectedFile().getName() != null) ? ChoixFichierVel.getSelectedFile().getPath() : "";
            tfParcourir.setText(MonFichierVel);

        }
    }//GEN-LAST:event_btnParcourirActionPerformed

